export function searchIcd10Codes(query: string, limit = 12): Icd10Code[] {
  const normalized = query.trim().toLowerCase();
  if (!normalized) {
    return [];
  }

  return icd10Codes
    .filter(
      (item) =>
        item.code.toLowerCase().includes(normalized) ||
        item.description.toLowerCase().includes(normalized),
    )
    .slice(0, limit);
}
